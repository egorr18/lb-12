# Дані про товари
sklad = [
    {"назва": "Молоко", "категорія": "Продукти", "кількість": 10, "ціна": 30},
    {"назва": "Сир", "категорія": "Продукти", "кількість": 3, "ціна": 100},
    {"назва": "Мило", "категорія": "Побутова хімія", "кількість": 2, "ціна": 20},
    {"назва": "Шампунь", "категорія": "Побутова хімія", "кількість": 6, "ціна": 80},
    {"назва": "Ручка", "категорія": "Канцелярія", "кількість": 15, "ціна": 10},
    {"назва": "Зошит", "категорія": "Канцелярія", "кількість": 4, "ціна": 15}
]

# Ліміт для замовлення
поріг = 5

# Загальна вартість по категоріях
категорії = {}

for товар in sklad:
    кат = товар["категорія"]
    вартість = товар["кількість"] * товар["ціна"]
    if кат in категорії:
        категорії[кат] += вартість
    else:
        категорії[кат] = вартість

# Пошук категорії з найбільшою вартістю
макс_кат = max(категорії, key=категорії.get)

# Товари для замовлення
до_замовлення = []
for товар in sklad:
    if товар["кількість"] < поріг:
        до_замовлення.append(товар)

# Вивід результатів
print("Вартість по категоріях:")
for кат, сума in категорії.items():
    print(f"{кат}: {сума} грн")

print(f"\nНайдорожча категорія: {макс_кат} ({категорії[макс_кат]} грн)")

print("\nТовари, яких мало:")
for товар in до_замовлення:
    print(f"{товар['назва']} ({товар['кількість']} шт)")
